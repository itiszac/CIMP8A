#! /usr/bin/env python3

# Incrementing
counter = 0
print("Incrementing starting point: " + str(counter))
counter = counter + 1
print("counter = " + str(counter))
counter += 1
print("counter = " + str(counter))

# Decrementing
count = 10
print("Decrementing starting point: " + str(count))
count = count - 1
print("counter = " + str(count))
count -= 1
print("counter = " + str(count))